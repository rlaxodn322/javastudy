package chap_02;

public class _02_Operator2 {
    public static void main(String[] args) {
        int num= 2;
        num = num + 2;
        System.out.println(num);

        num +=2;
        System.out.println(num);

        num = num - 2;
        System.out.println(num);

        num-=2;
        System.out.println(num);
    }
}
